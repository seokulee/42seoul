#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list *new_lst;

	new_lst = (t_list *)malloc(sizeof(t_list));
	if (!new_list)
		return (NULL);
	new_list->content = content;
	new_list->next = NULL;
	return(new_list);
}
